/**
 * Builds src/cards/format/generatedRegistry.ts from public/cards catalog JSON.
 *
 *   npm run format:registry
 *
 * Run after `npm run build:assets` when the local card catalog changes.
 */
import { existsSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const INDEX = resolve(ROOT, 'public', 'cards', 'index.json');
const OUT = resolve(ROOT, 'src', 'cards', 'format', 'generatedRegistry.ts');

function parseKind(standardLabel, extraLabel) {
  const standard = (standardLabel ?? '').toLowerCase();
  const extra = (extraLabel ?? '').toLowerCase();
  if (standard.includes('banned') || extra.includes('banned')) return 'banned';
  if (standard.includes('standard legal')) return 'standardLegal';
  if (standard.includes('standard not legal')) return 'standardNotLegal';
  return 'unknown';
}

function main() {
  if (!existsSync(INDEX)) {
    console.error('[format:registry] missing public/cards/index.json — run `npm run build:assets` first.');
    process.exitCode = 1;
    return;
  }

  const idx = JSON.parse(readFileSync(INDEX, 'utf8'));
  const byNumber = {};

  for (const { code } of idx.sets) {
    const setPath = resolve(ROOT, 'public', 'cards', 'sets', `${code}.json`);
    if (!existsSync(setPath)) continue;
    for (const card of JSON.parse(readFileSync(setPath, 'utf8'))) {
      byNumber[card.cardNumber] = parseKind(card.legality?.standard, card.legality?.extra);
    }
  }

  const lines = Object.entries(byNumber)
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([cardNumber, kind]) => `  '${cardNumber}': '${kind}',`);

  const source = `/**
 * Auto-generated by scripts/generate-format-registry.mjs — do not edit.
 * Regenerate with \`npm run format:registry\` after updating public/cards.
 */
export type CardFormatKind = 'standardLegal' | 'standardNotLegal' | 'banned' | 'unknown';

/** Tournament format status keyed by card number (5-1-2-3 identity). */
export const CARD_FORMAT_BY_NUMBER: Record<string, CardFormatKind> = {
${lines.join('\n')}
};
`;

  writeFileSync(OUT, source, 'utf8');
  console.log(`[format:registry] wrote ${Object.keys(byNumber).length} entries to ${OUT}`);
}

main();
